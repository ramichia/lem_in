#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*str;
	int		i;
	int		length;

	if (!s || !*s)
		return (NULL);
	length = ft_strlen(s);
	str = (char*)malloc(sizeof(char) * (length + 1));
	if (str != NULL)
	{
		i = 0;
		while (s[i])
		{
			str[i] = f(s[i]);
			i++;
		}
		str[i] = '\0';
		return (str);
	}
	return (NULL);
}
