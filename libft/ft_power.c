#include "libft.h"

int		ft_power(int c, int n)
{
	int		i;

	i = 0;
	if (n == 0)
		return (1);
	if (n < 0)
		return (0);
	if (0 < n)
		c = c * ft_power(c, n - 1);
	return (c);
}
